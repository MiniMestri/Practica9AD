//Variable global
var edad = 45;
console.log("Tu edad es de:");
console.log(edad);

//Variable local
let edad = 45;
edad = 46;
console.log("Tu edad es de: "+edad+" años");

//Variable constante
const EDAD = 45;
EDAD = 46;
console.log("Tu edad es de: "+EDAD+" años");

//Encadenamiento 
var edad = 45;
console.log("Tu edad es de: "+edad+" años");

//Incrementos
var edad = 45;
console.log("Tu edad es de: "+edad+" años");
edad++;
console.log("Tu edad es de: "+edad+" años");
edad--;
console.log("Tu edad es de: "+edad+" años");

//Operadores abreviados
var edad = 45;
console.log("Tu edad es de: "+edad+" años");
edad += 20; 
console.log("Tu edad es de: "+edad+" años");
edad -= 5;
console.log("Tu edad es de: "+edad+" años");
edad *= 2;
console.log("Tu edad es de: "+edad+" años");
edad /= 5;
console.log("Tu edad es de: "+edad+" años");